#Data types

# 1. Integers
a = 5
b = -10
print(type(a))  # Output: <class 'int'>
print(type(b))  # Output: <class 'int'>
print(a + b)  # Output: -5

 
# 2. Floats
c = 3.14
d = -2.5
print(type(c))  # Output: <class 'float'>
print(type(d))  # Output: <class 'float'>
print(c + d)  # Output: 0.6399999999999999

# 3. Strings
e = "Hello"
f = 'World' 
print(type(e))  # Output: <class 'str'>
print(type(f))  # Output: <class 'str'>
print(e + " " + f)  # Output: Hello World

# 4. Lists
g = [1, 2, 3]
h = ["apple", "banana", "cherry"]
print(type(g))  # Output: <class 'list'>
print(type(h))  # Output: <class 'list'>
print(g + h)  # Output: [1, 2, 3, 'apple', 'banana', 'cherry']

# 5. Tuples
i = (1, 2, 3)      
j = ("apple", "banana", "cherry")
print(type(i))  # Output: <class 'tuple'>
print(type(j))  # Output: <class 'tuple'>
print(i + j)  # Output: (1, 2, 3, 'apple', 'banana', 'cherry')    