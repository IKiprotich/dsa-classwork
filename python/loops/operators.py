#arithmetic operators
print(7/2)
print(7//2)

#assignment operators
a = 45
b = 90

a == b
a != b

#comparison/relational operators
a = 45
b = 90
print(a == b)
print(a != b)

#logical operators
a = True
b = False
print(a and b)

#Bitwise operators
a = 10
b = 4
print(a & b)
print(a | b)
print(a ^ b)
print(~a)
print(a << 2)
print(a >> 2)
#Identity operators
a = [1, 2, 3]
b = a
c = a[:]
print(a is b)
print(a is c)
print(a is not c)

#Membership operators
a = [1, 2, 3, 4, 5]
print(1 in a)
print(6 in a)
print(1 not in a)
print(6 not in a)
